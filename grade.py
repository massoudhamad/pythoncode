marks = int(input("Please enter your marks: "))

if marks >= 80:
    print("A\nExcellent")
elif marks >= 70:
    print("B+\nVery Good")
elif marks >= 60:
    print("B\nGood")
elif marks >= 50:
    print("C\nPass")
elif marks >= 40:
    print("D\nPoor")
else:
    print("F\nFail\nLazy\nYou are not part of us")
